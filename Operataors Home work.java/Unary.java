class Unary
{
	public static void main(String[]args)
	
	{
	int A = 5;
	
//	after A++  = 5+1     = 6;
//	after ++A  = 6+1     = 7;
//	after A--  = 7-1     = 6;
//	after --A  = 6-1     = 5;
	
	System.out.println("intial value:"+A);
	System.out.println("After A++:"+6);
	System.out.println("After --A:"+7);
	System.out.println("After A--:"+6);
	System.out.println("After --A:"+5);
	
	
	}
	
}